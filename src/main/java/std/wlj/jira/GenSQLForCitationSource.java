    static List<CitnData> readNextRep(Scanner scan) {
        List<CitnData> results = new ArrayList<>();

        boolean isSameRep = true;
        while (scan.hasNextLine()  &&  isSameRep) {
            String repData = scan.nextLine();
            String[] chunks = PlaceHelper.split(repData, '|');
            if (chunks.length > 8) {
                CitnData currRow = new CitnData(chunks);
                if (prevRow != null) {
                    if (! currRow.repId.equals(prevRow.repId)) {
                        results.add(prevRow);
                        isSameRep = false;
                    } else if (! currRow.citnId.equals(prevRow.citnId)) {
                        results.add(prevRow);
                    }
                }
                prevRow = currRow;
            }
        }

        return results;
    }

    static String updateCitnSQL(String citnId, String tranId, String srcRef, String oldSrcId, String newSrcId) {
        String newSrcRef = generateNewSrcRef(srcRef, oldSrcId);
        return String.format(updateSQL, newSrcId, newSrcRef, citnId, tranId);
    }

    protected static String generateNewSrcRef(String srcRef, String oldSrcId) {
        String newSrcRef = srcIdTitle.get(oldSrcId);
        if (srcRef == null  ||  srcRef.trim().isEmpty()) {
            // do nothing
        } else if (srcRef.trim().equals("null")) {
            // do nothing
        } else if (srcRef.startsWith("null")) {
            newSrcRef += ", " + srcRef.substring(4).replaceAll("'", "''").trim();
        } else {
            newSrcRef += ", " + srcRef.replaceAll("'", "''").trim();
        }

        if (newSrcRef.startsWith(", ")) {
            newSrcRef = newSrcRef.substring(2);
        }

        if (newSrcRef.isEmpty()) {
            if ("2".equals(oldSrcId)) {
                newSrcRef = "Official Web Site";
            } if ("59".equals(oldSrcId)) {
                newSrcRef = "Wikipedia.org";
            } else if ("421".equals(oldSrcId)) {
                newSrcRef = "Google.com";
            } else if ("440".equals(oldSrcId)) {
                newSrcRef = "books.google.com";
            }
        }

        return newSrcRef;
    }

